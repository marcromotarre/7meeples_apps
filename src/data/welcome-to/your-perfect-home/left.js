const getLeftCardNumber = ({ deck, discardDeck, key, value }) => {
  let leftCards = 0;
  if (key === "effect") {
    const lastEffects = [];
    if (discardDeck[0][0]) {
      lastEffects.push(discardDeck[0][0]);
    }
    if (discardDeck[1][0]) {
      lastEffects.push(discardDeck[1][0]);
    }
    if (discardDeck[2][0]) {
      lastEffects.push(discardDeck[2][0]);
    }
    leftCards += lastEffects.filter((card) => card[key] === value).length;
  }
  leftCards += deck[0]
    .concat(deck[1], deck[2])
    .filter((card) => card[key] === value).length;
  console.log(key, value, leftCards);

  return leftCards;
};
